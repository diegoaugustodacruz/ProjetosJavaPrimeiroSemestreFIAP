package br.com.fiap.tds.bean;

public class Cor {

	private String nome;
	
	private int r, g, b;
	
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setR(int r) {
		this.r = r;
	}
	
	public int getR() {
		return r;
	}
	
	public void setG(int g) {
		this.g=g;
	}
	
	public int getG() {
		return g;
	}
	
	public void setB(int b) {
		this.b = b;
	}
	
	public int getB() {
		return b;
	}
	
}
